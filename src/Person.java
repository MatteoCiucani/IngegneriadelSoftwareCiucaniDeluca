public class Person {
    public Person(String name, String address, String email, int phone, String gender, AccountType accountType) {
        this.name = name;
        this.address = address;
        this.email = email;
        this.phone = phone;
        this.gender = gender;
        this.accountType = accountType;
    }
    private String name;
    private String address;
    private String email;
    private int phone;
    private String gender;
    private AccountType accountType;


}
